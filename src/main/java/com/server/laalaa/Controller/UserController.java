  // GENERER LE MATRICULE D'UN UTILISATEUR.
  public String GenerateMatricule(){
     String mat="";  // le matricule generer est su 4 caracteres
     Long cnt;
     cnt = usersRepository.count();

     switch (String.valueOf(cnt).length()){
       case 1 : mat="000"+cnt;
       case 2 : mat="00"+cnt;
       case 3 : mat="0"+cnt;
       case 4 : mat=""+cnt;
     }
     return mat;
  }


